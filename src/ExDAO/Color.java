package ExDAO;

public class Color {
    // Text colors
    public static final String RESET = "\u001B[0m";  // Reset color
    public static final String BLACK = "\u001B[30m";  // Black
    public static final String RED = "\u001B[31m";    // Red
    public static final String GREEN = "\u001B[32m";  // Green
    public static final String YELLOW = "\u001B[33m"; // Yellow
    public static final String BLUE = "\u001B[34m";   // Blue
    public static final String MAGENTA = "\u001B[35m"; // Magenta
    public static final String CYAN = "\u001B[36m";   // Cyan
    public static final String WHITE = "\u001B[37m";  // White
    // Bright text colors
    public static final String BRIGHT_BLACK = "\u001B[90m";  // Bright Black
    public static final String BRIGHT_RED = "\u001B[91m";    // Bright Red
    public static final String BRIGHT_GREEN = "\u001B[92m";  // Bright Green
    public static final String BRIGHT_YELLOW = "\u001B[93m"; // Bright Yellow
    public static final String BRIGHT_BLUE = "\u001B[94m";   // Bright Blue
    public static final String BRIGHT_MAGENTA = "\u001B[95m"; // Bright Magenta
    public static final String BRIGHT_CYAN = "\u001B[96m";   // Bright Cyan
    public static final String BRIGHT_WHITE = "\u001B[97m";  // Bright White
    public static final String BRIGHT_GRAY = "\u001B[37m";

}